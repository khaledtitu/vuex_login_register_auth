<template>
  <div id="app">
    <div class="container">
      <!--UPLOAD-->
      <form enctype="multipart/form-data" novalidate v-if="isInitial || isSaving">

        <h2 class="title pb-3">Upload images</h2>
        <div class="dropbox">
          <input type="file" multiple :name="uploadFieldName" :disabled="isSaving" @change="filesChange($event.target.name, $event.target.files); fileCount = $event.target.files.length"
            accept="image/*" class="input-file">
            <p v-if="isInitial">
              Drag your file(s) here to begin<br> or click to browse
            </p>
            <p v-if="isSaving">
              Uploading {{ fileCount }} files...
            </p>
        </div>
      </form>
      <!--SUCCESS-->
      <div v-if="isSuccess">
        <h2>Uploaded {{ uploadedFiles.length }} file(s) successfully.</h2>
        <p>
          <a href="javascript:void(0)" @click="reset()">Upload again</a>
        </p>
        <v-layout row wrap>
          <v-flex v-for="(item, index) in uploadedFiles" :key="index" xs4>
            <v-card flat tile>
              <v-img :src="item.url" height="150px" :alt="item.originalName" ></v-img>
            </v-card>
          </v-flex>
        </v-layout>
      </div>
      <!--FAILED-->
      <div v-if="isFailed">
        <h2>Uploaded failed.</h2>
        <p>
          <a href="javascript:void(0)" @click="reset()">Try again</a>
        </p>
        <pre>{{ uploadError }}</pre>
      </div>
      <div class="test">
        <!-- <v-btn color="primary" @click="saveImages"> Save images </v-btn> -->
      </div>
    </div>
  </div>
</template>

<script>
import { upload } from './file-upload.service' // real service

const STATUS_INITIAL = 0
const STATUS_SAVING = 1
const STATUS_SUCCESS = 2
const STATUS_FAILED = 3

export default {
	name: 'ImageInput',
	data() {
		return {
			uploadedFiles: [],
			uploadError: null,
			currentStatus: null,
			uploadFieldName: 'photos'
		}
	},
	computed: {
		isInitial() {
			return this.currentStatus === STATUS_INITIAL
		},
		isSaving() {
			return this.currentStatus === STATUS_SAVING
		},
		isSuccess() {
			return this.currentStatus === STATUS_SUCCESS
		},
		isFailed() {
			return this.currentStatus === STATUS_FAILED
		}
	},
	methods: {
		reset() {
			// reset form to initial state
			this.currentStatus = STATUS_INITIAL
			this.uploadedFiles = []
			this.uploadError = null
		},
		save(formData) {
			// upload data to the server
			this.currentStatus = STATUS_SAVING
			upload(formData)
				.then(x => {
					this.uploadedFiles = [].concat(x)
					this.currentStatus = STATUS_SUCCESS
				})
				.catch(err => {
					this.uploadError = err.response
					this.currentStatus = STATUS_FAILED
				})
		},
		filesChange(fieldName, fileList) {
			// console.log(fileList)
      if (fileList.length <= 3) {
       // this.$store.commit('auth/allImageData', fileList)
       // console.log(fieldName, fileList)
        // handle file changes
        const formData = new FormData()
        if (!fileList.length) return
        // append the files to FormData
        Array
          .from(Array(fileList.length).keys())
          .map(x => {
            formData.append(fieldName, fileList[x], fileList[x].name)
          })
        // save it
        this.save(formData)

      }else {
          
          this.$toast.error({
            title: 'You can upload maximum 3 images'
          })
          return false 
      }
		},
    saveImages() {
